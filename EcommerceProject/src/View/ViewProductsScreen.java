    public ViewProductsScreen() {
        initComponents();
        ArrayList<ProductItem> productsArrayList = new ArrayList<>();
        ViewProductViewModel viewProductViewModel = new ViewProductViewModel();
        Connection connection;
        DefaultTableModel t= (DefaultTableModel) jTable1.getModel();
        String name;
        double price;
        int id;
        try {
            connection = DriverManager.getConnection("jdbc:sqlite:productsDataBase.db");
            productsArrayList = viewProductViewModel.getProducts();
            for (int i = 0; i < productsArrayList.size(); i++) 
            {
               name = productsArrayList.get(i).getName();
               price = productsArrayList.get(i).getPrice();
               id = productsArrayList.get(i).getId();               
               Object data[]={id, name, price};
               t.addRow(data);
            } 
        } 
        catch (SQLException ex) {
            Logger.getLogger(ViewProductsScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
